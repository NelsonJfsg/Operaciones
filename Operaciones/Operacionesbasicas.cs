using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Operaciones
{
    public class Operacionesbasicas
    {
        public static Double suma(double num0, double num1)
        {
            return (num0 + num1);
        }

        public static Double resta(double num0, double num1)
        {
            return (num0 - num1);
        }
        public static Double mult(double num0, double num1)
        {
            return (num0 * num1);
        }
        public static Double div(double num0, double num1)
        {
            return (num0 / num1);
        }
    }
}
